import { COLORS } from "@/lib/data"
import { getRandomColorsForPlayer } from "@/lib/get-radom-color-for-player"
import type { Song } from "@/types"
import { create } from "zustand"

type PlayerStore = {
	currentSong: Song | null
	isPlaying: boolean
	queue: Song[]
	currentIndex: number
	timeLeft: number
	// Меняем цвет плеера
	changeColors: string

	setTimeLeft: (time: number) => void

	initializeQueue: (songs: Song[]) => void
	playAlbum: (songs: Song[], startIndex?: number) => void
	setCurrentSong: (song: Song | null) => void
	togglePlay: () => void
	playNextSong: () => void
	playPreviousSong: () => void
}

export const usePlayerStore = create<PlayerStore>()((set, get) => ({
	changeColors: COLORS[0],
	timeLeft: 0,
	currentSong: null,
	currentIndex: -1,
	isPlaying: false,
	queue: [],

	setTimeLeft: (time: number) => {
		set({ timeLeft: time })
	},

	initializeQueue: (songs: Song[]) => {
		set({ queue: songs, currentSong: get().currentSong || songs[0], currentIndex: get().currentIndex === -1 ? 0 : get().currentIndex })
	},
	playAlbum: (songs: Song[], startIndex = 0) => {
		if (songs.length === 0) return
		const song = songs[startIndex]
		set({
			queue: songs,
			currentSong: song,
			currentIndex: startIndex,
			isPlaying: true,
		})
	},
	setCurrentSong: (song: Song | null) => {
		if (!song) return
		const songIndex = get().queue.findIndex((s) => s._id === song._id)
		set({
			currentSong: song,
			currentIndex: songIndex !== -1 ? songIndex : get().currentIndex,
			isPlaying: true,
		})
	},
	togglePlay: () => {
		const willStartPlaying = !get().isPlaying

		set({ isPlaying: willStartPlaying })
	},
	playNextSong: () => {
		if (get().queue.length === 0) return
		const { currentIndex, queue } = get()
		const nextIndex = currentIndex + 1
		if (nextIndex < queue.length) {
			const nextSong = queue[nextIndex]
			set({
				currentSong: nextSong,
				currentIndex: nextIndex,
				isPlaying: true,
				changeColors: getRandomColorsForPlayer(),
			})
		} else {
			set({ isPlaying: false })
		}
	},
	playPreviousSong: () => {
		if (get().queue.length === 0) return
		const { currentIndex, queue } = get()
		const prevIndex = currentIndex - 1
		if (prevIndex >= 0) {
			const prevSong = queue[prevIndex]
			set({
				currentSong: prevSong,
				currentIndex: prevIndex,
				isPlaying: true,
				changeColors: getRandomColorsForPlayer(),
			})
		} else {
			set({ isPlaying: false })
		}
	},
}))
